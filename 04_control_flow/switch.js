const month = "feb"

switch (month) {

    case "Jan":
        console.log("January");
        break;

    case "feb":
        console.log("Fabruary");
        break;

    case "march":
        console.log("march");
        break;

    case "april":
        console.log("April");
        break;

        
    default:
        console.log("default case match");
        break;
}